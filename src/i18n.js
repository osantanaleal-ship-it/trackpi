function detectLanguage() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY)
    if (stored && dictionaries[stored]) return stored
  } catch {
    // Ignore storage access issues and fall back to browser detection.
  }
  const browser = (typeof navigator !== 'undefined' && (navigator.language || navigator.languages?.[0]) || 'es').slice(0, 2).toLowerCase()
  return dictionaries[browser] ? browser : 'es'
}

let currentLang = detectLanguage()

function interpolate(template, vars) {
  if (!vars) return template
  return template.replace(/\{(\w+)\}/g, (_, key) => (vars[key] != null ? String(vars[key]) : ''))
}

export function translate(key, vars, lang = currentLang) {
  const dict = dictionaries[lang] || dictionaries.es
  const value = dict[key] ?? dictionaries.es[key]
  if (value == null) return key
  return interpolate(value, vars)
}

export function getLanguage() {
  return currentLang
}

export function getLocale(lang = currentLang) {
  return LOCALES[lang] || 'es-ES'
}

export function placeKind(osmValue, lang = currentLang) {
  if (!osmValue) return ''
  const dict = dictionaries[lang] || dictionaries.es
  return dict.kinds?.[osmValue] || dictionaries.es.kinds[osmValue] || ''
}

function applyDocumentLang(lang) {
  if (typeof document !== 'undefined') document.documentElement.lang = lang
}

const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(currentLang)

  useEffect(() => {
    applyDocumentLang(lang)
  }, [lang])

  const changeLanguage = useCallback((next) => {
    if (!dictionaries[next]) return
    currentLang = next
    try {
      localStorage.setItem(STORAGE_KEY, next)
    } catch {
      // A device that blocks storage can still switch language for this session.
    }
    setLang(next)
  }, [])

  const t = useCallback((key, vars) => translate(key, vars, lang), [lang])

  return createElement(I18nContext.Provider, { value: { lang, setLang: changeLanguage, t, locale: getLocale(lang) } }, children)
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error('useI18n must be used within I18nProvider')
  return context
}
